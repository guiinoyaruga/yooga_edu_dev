let one = 1
let two = 2


// elementos de comparação
// == -> igual a...
// != -> diferente de...

console.log(one == 2)
console.log (one == '1')

console.log (one != 1)
console.log (two != 1)

// === -> estritamente igual e do mesmo tipo de variável
// !== -> estritamente diferente e do mesmo tipo de variável

console.log (two !== '2')
console.log (two !== 2)